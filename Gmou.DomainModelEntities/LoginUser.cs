using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Gmou.DomainModelEntities
{
    public class LoginUser
    {
        public string UserName { get; set; }
        public string Password { get; set; }
        public string Role { get; set; }
        public int EmployeeLoginID { get; set; }
        public int DepartmentID { get; set; }
      
        public LoginUser() { }
        public LoginUser(string username,string password, string role,int departmentid, int loginemployeeid)
        {
            this.UserName=username;
            this.Password=password;
            this.Role = role;
            this.DepartmentID = departmentid;
            this.EmployeeLoginID = loginemployeeid;


        }
    }
}
